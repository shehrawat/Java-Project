package Loops;

public class While {

	public static void main(String[] args) 
	{
		int i = 1;	
						// In while loop it first check the condition then print the statement
		
		while (i<=10)  // here it check only for true value
//		while (true) -> for infinite loop 
		{
			System.out.println(i);
			i++;
		}
	}

}
